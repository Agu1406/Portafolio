// Estructura básica de una clase en Java.
public class Estructura {
    public static void main(String[] args) { // Bloque, aquí empieza el main

        boolean hoyEsDomingo = true;
        boolean hoyNoLlueve = false;

        if (hoyEsDomingo) { // Aquí empieza el if.

            System.out.println("¡Hola clase!");

        } // Aquí termina el if.

        if (hoyEsDomingo && hoyNoLlueve) { // Aquí empieza el if.

            System.out.println("Me voy a pescar.");

        } // Aquí termina el if.

    } // Aquí termina el main.


} // Fin de la estructura básica de una clase en Java.
