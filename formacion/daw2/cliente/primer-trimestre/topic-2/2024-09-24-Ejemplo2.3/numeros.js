// Ejemplos de números en JavaScript
console.log("=== NÚMEROS EN JAVASCRIPT ===");

// Números enteros
let entero = 42;
console.log("Número entero:", entero);

// Números decimales
let decimal = 3.14;
console.log("Número decimal:", decimal);

// Números negativos
let negativo = -10;
console.log("Número negativo:", negativo);

// Operaciones básicas
console.log("\nOperaciones básicas:");
console.log("Suma:", 5 + 3);
console.log("Resta:", 10 - 4);
console.log("Multiplicación:", 6 * 2);
console.log("División:", 15 / 3);

// Valores especiales
console.log("\nValores especiales:");
console.log("Infinito:", Infinity);
console.log("Infinito negativo:", -Infinity);
console.log("No es un número (NaN):", NaN);

// Métodos útiles
let numero = 123.456;
console.log("\nMétodos útiles:");
console.log("Redondear:", Math.round(numero));
console.log("Número máximo:", Math.max(1, 5, 3, 2));
console.log("Número mínimo:", Math.min(1, 5, 3, 2));
console.log("Número aleatorio entre 0 y 1:", Math.random()); 